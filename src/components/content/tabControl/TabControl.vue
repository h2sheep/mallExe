<template>
  <div class="tab-control">
    <div
      v-for="(item, index) in titles"
      class="tab-control-item"
      :class="{active:index === curIndex}"
      @click="itemClick(index)"
    >
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
  export default {
    name: 'TabControl',
    props: {
      titles: {
        type: Array,
        default() {
          return []
        }
      }
    },
    data() {
      return {
        curIndex: 0
      }
    },
    methods: {
      itemClick(index) {
        this.curIndex = index
        this.$emit('tabClick', index)
      }
    }
  }
</script>

<style lang="less">
  .tab-control {
    display: flex;
    text-align: center;
    background-color: #fff;

    .tab-control-item {
      flex: 1;
      height: 44px;
      line-height: 44px;

      span {
        padding: 3px;
      }

      &.active {
        color: pink;
      }
      &.active span {
        border-bottom: 2px solid pink;
      }
    }
  }
</style>
